    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        for(ProdPedEstoque ppe : prodpedes)
        {
            if(ppe.getQuantidade() == ppe.getQuantidadePedAtend())
                ppe.setStat(1); //Atendido totalmente
            else if(ppe.getQuantidadePedAtend() > 0 && ppe.getQuantidadePedAtend()< ppe.getQuantidade())
                ppe.setStat(2); //Parcialmente atendido
            else
                ppe.setStat(3); //Cancelado
        }
        
        Date datahoje = new Date();
        pedido.setDataAtend(datahoje);
        boolean persistiu = new ControlaPedidoEstoque().persistePedidoEstoque(pedido);
        boolean erro_cadastro = !persistiu;       
        
        for(ProdPedEstoque ppe : prodpedes)
        {
            persistiu = new ControlaProdPedEstoque().persisteProdPedEstoque(ppe);
            if(!persistiu)
            {
                erro_cadastro = true;
            }
        }
        
        if(!erro_cadastro){
            JOptionPane.showMessageDialog(null, "Pedido Confirmado com Sucesso!");
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao salvar alterações!");
        }
        
        panelpai.updatePedidosEstoque();
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

