    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTable1.getSelectedRow() == -1)
            return;
        
        int quantidade_atendida = Integer.parseInt(jTextField1.getText());
        int quantidade_demandada = prodpedes.get(jTable1.getSelectedRow()).getQuantidade();
        
        if(quantidade_atendida <= quantidade_demandada && quantidade_atendida > 0)
            prodpedes.get(jTable1.getSelectedRow()).setQuantidadePedAtend(quantidade_atendida);
        else
            JOptionPane.showMessageDialog(null, "Digite um valor válido!");
        
        updateListaProduto();
    }//GEN-LAST:event_jButton1ActionPerformed

