    public void updateListaProduto()
    {        
        DefaultTableModel model = new DefaultTableModel();
        String[] colunas = new String[3];
        
        model.setColumnCount(3);
        colunas[0] = "Produto";
        colunas[1] = "Quantidade solicitada";
        colunas[2] = "Quantidade atendida";

        model.setColumnIdentifiers(colunas);
        
        for(ProdPedEstoque ppe : prodpedes)
        {
            Object[] obe = new Object[3];
            
            for(Produto p : produtos)
                if(ppe.getProduto().getProdId().contentEquals(p.getProdId()))
                    obe[0] = p.getNomeProd();
            
            obe[1] = ppe.getQuantidade();
            obe[2] = ppe.getQuantidadePedAtend();
            
            model.addRow(obe);
        }
        
        this.jTable1.setModel(model);
        this.jTable1.setFocusable(false);
        this.jTable1.setRowSelectionAllowed(true);
    }
    
